import Foundation

extension String {
    /// Escapes text placed between framework-owned prompt tags. Quotes remain
    /// readable because they cannot close or forge a text-only XML boundary.
    var promptTagEscaped: String {
        replacingOccurrences(of: "&", with: "&amp;")
            .replacingOccurrences(of: "<", with: "&lt;")
            .replacingOccurrences(of: ">", with: "&gt;")
    }

    /// Escapes `&`, `<`, `>`, `"`, and `'` for safe embedding in HTML or XML
    /// text and attribute contexts.
    var htmlEscaped: String {
        replacingOccurrences(of: "&", with: "&amp;")
            .replacingOccurrences(of: "<", with: "&lt;")
            .replacingOccurrences(of: ">", with: "&gt;")
            .replacingOccurrences(of: "\"", with: "&quot;")
            .replacingOccurrences(of: "'", with: "&#39;")
    }
}
